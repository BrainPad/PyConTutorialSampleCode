# -*- coding: utf-8 -*-

# 整数型(int)
print(type(1))
# 浮動小数点型(float)
print(type(3.14))
# 複素数型(complex)
print(type(1+5j))

# 論理型(bool)
print(type(True))
print(True and False)
print(True or False)
print(not False)
print(2 == 2)
print(2 > 3)
print('2 > 3', 2 > 3)

# 文字列型(str)
print(type('テスト'))
print('テスト')
print("テスト")
print("""テス
ト""")

